//Every java file must have a class with the same name as the file
public class If {

    //The function that is called as soon as the file is executed
    public static void main(String[] args) {
        //A boolean variable that is true
        boolean comparison = true;

        //Check to see if comparison is true
        if(comparison) {
            //Print out that comparison is true
            System.out.println("comparison is true");

        //Since comparison is false, then execute this code
        } else {
            //Print out that comparison is false
            System.out.println("comparison is false");
        }
    }
}
